		<ol>
			<li>Dans les exemples ci-dessus, l&agrave; o&ugrave; la semi-voyelle est suivie par une
			voyelle (de&#365; derni&egrave;res colonnes), on consid&egrave;re g&eacute;n&eacute;ralement qu'elle
			appartient &agrave; la syllabe suivante o&ugrave; elle reprend son r&ocirc;le de consonne.
			Il n'y a donc pas diphtongue au sens phon&eacute;tique du terme.</p>
			</p>
			<p>
			<li>La r&eacute;alisation exacte des diphtongues peut varier selon les
			locuteurs. Certains auront tendance &agrave; conserver une prononciation
			consonantique de la semi-voyelle, surtout s'il s'agit de "j", alors que
			d'autres la prononcent comme une voyelle tr&egrave;s br&egrave;ve plus ou moins fondue
			avec le premier &eacute;l&eacute;ment. Par exemple pour la diphtongue "aj", on peut
			aussi bien se baser sur la prononciation de l'anglais "my", ou de
			l'allemand "mein", que sur celle du fran&ccedil;ais "maille", de l'espagnol
			"hay" ou du russe "ra&iuml;".</p>
			</p>
			<p>
			<li>Les autres combinaisons de voyelle et de semi-voyelle sont
			extr&ecirc;mement rares. La combinaison "o&#365;" se trouve dans quelques mots. La
			combinaison "ij" se trouve dans des noms propres russes translitt&eacute;r&eacute;s
			(ex. &#264;ajkovskij).</p>
			</p>
			<p>
			<li>Les combinaisons "ai", "au", "oi", "ou", "ii", "iu" etc. -- qui ne sont
			pas rares en esp&eacute;ranto -- <b>ne sont pas</b> des diphtongues, mais bel et
			bien de&#365; voyelles j&#365;tapos&eacute;es, formant donc de&#365; syllabes : <br>
