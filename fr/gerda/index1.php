		<div class="rim"><b>Remarques :</b>
		<ol>
		<li>Il n'y a que tr&egrave;s peu de racines en esp&eacute;ranto qui aient une consonne
		double. Un exemple est "Finno", un Finlandais. Comme chaque lettre se
		prononce, on dit FIN-no. Certains mots compos&eacute;s peuvent avoir une
		consonne double, par exemple "Pollando", Pologne, de "Polo" + "lando",
		et on prononce Pol-LAN-do.</p>
		</p>
		<p>
		<li>Les consonnes sont peu influenc&eacute;es par le contexte. Les consonnes
		sonores en finale ne s'assourdissent pas, pas plus qu'au contact d'une
		consonne sourde : "abstrakta" se prononce abSTRAKta plut&ocirc;t que
		apSTRAKta. Les sourdes ne deviennent pas sonores : dans "kapitalismo"
		le "s" ne se prononce pas comme un "z".
		</ol>
