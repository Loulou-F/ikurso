			<p>
			Cet aide-m&eacute;moire fran&ccedil;ais-esp&eacute;ranto contient tous les mots utilis&eacute;s dans le
			cours <i>Gerda malaperis</i>. Il vous aidera &agrave; retrouver des mots que
			vous avez &eacute;tudi&eacute;s mais que vous auriez momentan&eacute;ment oubli&eacute;s.
			<P>
			Nous attirons l'attention des &eacute;tudiants du cours sur le fait qu'ils ne
			doivent utiliser dans leur devoirs que des mots d&eacute;j&agrave; &eacute;tudi&eacute;s. C'est
			une condition indispensable pour bien m&eacute;moriser le vocabulaire de base
			et apprendre &agrave; en tirer le meilleur parti. &Agrave; cet effet, un nombre
			entre parenth&egrave;ses indique pour chaque entr&eacute;e le num&eacute;ro de chapitre o&ugrave;
			le mot correspondant est introduit. Par exception, quelques mots
			utiles n'apparaissant pas dans <i>Gerda</i> ont &eacute;t&eacute; ajout&eacute;s.
			<P>
			Nous avons essay&eacute; de rendre ce lexique suffisamment pr&eacute;cis,
			notamment en diff&eacute;renciant autant que possible les homonymes. Ainsi
			nous indiquons fr&eacute;quemment entre parenth&egrave;ses un &eacute;l&eacute;ment suppl&eacute;mentaire
			permettant de distinguer plusieurs sens d'un m&ecirc;me mot fran&ccedil;ais. Par
			exemple :
			  <DL>
			  <DT><TT>fait (n.)</TT>
			  <DD>comprendre qu'il s'agit d'un nom substantif (&#171;c'est un fait
			  indubitable&#187;) et non du participe pass&eacute; (&#171;c'est fait&#187;)
			  <DT><TT>avant (adv.)</TT>
			  <DD>comprendre qu'il s'agit de l'adverbe (&#171;il est pass&eacute; avant&#187;) et
			  non de la pr&eacute;position (&#171;il est pass&eacute; avant nous&#187;)
			  <DT><TT>&eacute;tat (situation)</TT>
			  <DD>comprendre que ce mot ne convient pas pour d&eacute;signer un &eacute;tat au
			  sens politique (&#171;l'&Eacute;tat c'est moi&#187;, &#171;un &eacute;tat europ&eacute;en&#187;)
			  <DT><TT>etc.</TT>
			  </DL>
				<P>  
				N&eacute;anmoins il n'est pas toujours possible dans un petit lexique comme
				celui-ci de diff&eacute;rencier simplement de&#365; homonymes et l'&eacute;tudiant
				est invit&eacute; &agrave; rechercher syst&eacute;matiquement dans le chapitre concern&eacute; des
				exemples d'utilisation des mots indiqu&eacute;s. Par exemple, avec les
				entr&eacute;es suivantes :
				<UL>
				<PRE><DL>
				<DT>recevoir (11) <A HREF="&#265;ap11.php"><B>akcepti</B></A>
				<DT>recevoir (12) <A HREF="&#265;ap12.php"><B>ricevi</B></A>
				</DL></PRE>
				</UL>
				
				on aurait tort de conclure que <B>akcepti</B> et <B>ricevi</B> sont
				parfaitement synonymes.
				<P>
				<B>Avertissement :</B><BR>
				Dans ce lexique les verbes sont cit&eacute;s &agrave; l'infinitif, les adjectifs
				sont cit&eacute;s au masculin et les expressions compos&eacute;es de plusieurs mots
				sont cit&eacute;es sous l'initiale de leur premier mot (par exemple, tous les
				verbes pronomina&#365; se trouvent sous l'initiale 's').

