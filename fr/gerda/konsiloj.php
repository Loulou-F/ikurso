	<h3 id="movado">La E-a movado : kio kaj kia &#285;i estas?</h3>
	
	<p>
	  Homoj ofte fantazias pri la Esperanta movado. &#264;u &#285;i ne estas ia
	  sekto, a&#365; ia elito, kiu strebas influi la nuntempan socion? &#264;u &#285;i
	  ne similas al tiu Societo de Lumser&#265;antoj, pri kiu parolas la
	  a&#365;toro de <i>Gerda</i>? Ne havu tro da iluzioj... Venu, rigardu,
	  ju&#285;u mem, kio estas bona, kio estas malbona, kiuj estas allogaj
	  homoj, kiuj estas mallogaj, kiuj estas inteligentaj, kiuj estas
	  stultaj, kiuj perfekte parolas la lingvon, kiuj apena&#365; balbutas
	  &#285;in ktp. Vi povos nur miri pro la diverseco de la homoj, de la
	  kulturoj kaj de la celoj..., sed anka&#365; mirigos vin la fratecaj
	  ligoj, kiuj facile aperos inter vi kaj kelkaj a&#365; pluraj homoj el
	  tiu multfaceta komunumo.</p>
	
	<p>
	  Kiajn homojn vi renkontos en la Movado? &#264;iajn. Bela okazo por
	  restudi la korelativojn, &#265;u ne? Tamen necesas scii, ke en Okcidenta
	  E&#365;ropo la Movado estas sufi&#265;e maljuna. Meza&#285;uloj ne havas tempon
	  partopreni en la agadoj, do plejofte restas nur la emeritoj, de unu
	  flanko, kaj la junuloj, de alia flanko. Bon&#349;ance Azio, Afriko kaj
	  Orienta E&#365;ropo reekvilibrigas la a&#285;an piramidon.</p>
	
	<p>
	  La noblaj oficialaj celoj de la Movado estas sufi&#265;e konataj :
	  disvastigi la lingvon, promocii interhoman pacon kaj amon, ktp. Al
	  tio aldoni&#285;as la pli egoismaj celoj de &#265;iu aparta membro : voja&#285;i
	  eskterlanden, konati&#285;i kun aliaj kulturoj, trovi vivkunul(in)on
	  (eventuale nur dumkongresan), agrable pasigi la tempon, trovi publikon
	  por siaj artaj talentoj, ag-kampon por sia pov-soifo, ktp. Nepre
	  venu kun <i>viaj</i> celoj&nbsp;!</p>
	
	<p>
	  Kiel eniri la Movadon? Sufi&#265;as kapabli paroli Esperanton kaj ali&#285;i
	  al iu ajn Esperanto-asocio. Eble timigos vin la granda nombro de
	  asocioj, kiuj atendas vin... Ne timu, estas tre simple. Ekzistas nur
	  unu grandskala internacia asocio : 
	  <a href="http://www.uea.org">UEA (Universala Esperanto-Asocio)</a>.
	  Ali&#285;i al &#285;i estas preska&#365; nepra por &#171;vera&#187; Esperantisto : via
	  pago rajtigos vin ricevi la interesan pri-movadan
	  monatan revuon <i>Esperanto</i>, ricevi la junularan monatan gazeton
	  <i>Kontakto</i> kaj ricevi
	  jarlibron tricentpa&#285;an, kiu kolektas adresojn de preska&#365; &#265;iuj
	  asocioj kaj de preska&#365; 2000 delegitoj en preska&#365; 100 landoj.
	  Ekzistas malpli kostaj kotizoj sed, kompreneble, kun malpli da
	  servoj.</p>
	
	<p>
	  Anka&#365; pripensu la eblecon ali&#285;i al <i>nacia asocio</i>. La
	  kotizoj estas &#285;enerale pli malaltaj ol en UEA, la revuoj, bultenoj,
	  ktp malpli interesaj, sed ili estas pli proksimaj al vi, ili
	  subtenas la Esperantan vivon en via lando, proponas bonan
	  libroservon, renkonti&#285;ojn, ejojn ktp. Se viaj monaj rimedoj permesas tion
	  al vi, ali&#285;u kaj al UEA, kaj al via nacia asocio a&#365; al ties
	  junulara sekcio.</p>
	
	<p>
	  Se tio ne sufi&#265;as, vi povas ali&#285;i anka&#365; al <i>&#171;fakaj&#187; asocioj</i>
	  pli-malpli seriozaj, kiuj vere abundas : blinduloj, fervojistoj,
	  sennaciistoj, po&#349;tistoj, naturistoj, radio-amatoroj, kaj dekoj da
	  aliaj... Plejofte la kotizoj estas vere simbolaj.</p>
	
	<p>
	  Multaj asocioj anka&#365; organizas kursojn, seminariojn, ekzamenojn,
	  libroservon, korespondan servon, &#265;iaspecajn aran&#285;ojn kaj e&#265;
	  kongresojn. &#264;iujare UEA organizas tutmondan kongreson (oni diras :
	  <i>universalan kongreson</i>, a&#365; UK-on), kiu da&#365;ras unu semajnon
	  en julio-a&#365;gusto. Aparte konsilindaj estas la
	  internaciaj junularaj kongresoj (IJK), kiuj okazas samepoke, sed ne
	  &#265;iam samloke kiel la UK-oj.</p>
	
	
	<h3 id="konkludo">Konkludo : ali&#285;u!</h3>
	
	<p>
	  La Movado ege bezonas kapablajn homojn. Vi sendube estas tiaj. Se la
	  lingvo interesis vin sufi&#265;e, por ke vi investu kelkajn dekojn da
	  horoj en &#285;ian studon, vi deziros konati&#285;i kun la homoj, kiuj
	  parolas &#285;in kaj tenas &#285;in viva. Nature vi i&#285;os unu el ili, unu el
	  ni. &#284;is balda&#365;&nbsp;!</p>
	
