	<h3 id="kiel">Kiel lerni plu?</h3>
	<p>
	  Vi jam havas bonan nivelon en la lingvo. Tion &#265;i atestas via
	  kapablo legi senprobleme &#265;i tiun teksteton (la vortoj, kiuj ne
	  trovi&#285;as en <i>Gerda</i>, ricevas tradukon fine de tiu &#265;i pa&#285;o).
	  Vi jam konas la tutan bazan gramatikon kaj la plej oftajn vortojn.
	  Tamen vi ankora&#365; bezonas firmigi viajn konojn kaj akiri kelkajn
	  pliajn. Kiel do lerni plu&nbsp;?
	
	<p>
	  Unua konsilo : <strong>parolu&nbsp;!</strong> Unuhora babilado kun kompetentaj
	  Esperantoparolantoj valoras pli ol dek horoj da individua lernado.
	  Tamen kelkaj inter vi eble ne trovos tiajn homojn en sia najbara&#309;o.
	  Jen solvoj por vi.

	<ul>
		<li>
			Unue, sciu, ke multaj Esperanto-asocioj okazigas regulajn
		    renkonti&#285;ojn a&#365; konversaciajn rondojn. Kelkaj estas tre sukcesaj
		    kaj famaj, aliaj malpli, &#265;ar la laboro de la gvidanto estas tre
		    grava kaj malfacila : li (a&#365; &#349;i) devas paroligi la timemulojn,
		    takte silentigi la troparolemulojn kaj la tedulojn, kaj
		    &#265;iamaniere igi, ke &#265;iuj partoprenantoj sentu sin komforte
		    malgra&#365; la diferencoj de lingva nivelo, de a&#285;oj a&#365; de
		    interesoj.</li>
	
		    <li>Due, se vi estas junulo a&#365; meza&#285;ulo, vi nepre partoprenu
		    internacian junularan Esperanto-aran&#285;on. Tio estas la
		    plej bona medio por paroli kaj a&#365;skulti.</li>
		
		    <li>Trie, estas eblecoj telefone babili kun korektantoj de niaj
		    kursoj. Tiu &#265;i ebleco nuntempe estas sufi&#265;e limigita, sed
		    espereble kreskos, kiam la rimedoj de interneta telefonado
		    fari&#285;os sufi&#265;e maturaj.</li>
	</ul>
	
	  <p>Dua konsilo : <strong>restudu&nbsp;!</strong> Jes, restudu la lecionojn de
	  unua kurso, relegu la &#265;apitrojn de <i>Gerda</i>, rerakontu al vi
	  la rakonton, relegu niajn korektojn... Cetere ni kelkfoje aldonas
	  novajn metodikajn helpilojn al la kurso : ekz-e
	  <a href="../cge/vocabula.php">franca-esperanta leksikono</a>, nova 
	  <a href="../cge/temaro.php">la&#365;tema indekso</a> de la kurso DLEK kaj
	  oni preparas plenan &#171;konkordancon&#187; de <i>Gerda</i> kaj de
	  <i>Lasu</i>. &#264;io &#265;i estos al vi utila, firma bazo, precipe kiam vi
	  renkontos homojn, kiuj malbone uzas nian lingvon.</p>
	
	  <p>Kaj tio &#265;i enkondukas nian trian konsilon : <strong>estu singarda&nbsp;!</strong> Esperanto
	  estas ja facila lingvo. Pro tio kelkaj homoj nur lernas la kernon de
	  la lingvo kaj ili tuj imagas, ke ili jam &#265;ion konas. Kelkaj e&#265; ne
	  hezitas pedante disvastigi siajn teoriojn pri lingvo, kiun ili
	  neniam serioze studis. Ne sekvu ilin blinde kaj ne hezitu peti
	  konsilojn de niaj korektantoj a&#365; de &#171;bonaj a&#365;toroj&#187;.</p>
	
	  <p>Kvara konsilo : <strong>korespondu&nbsp;!</strong> Korespondi kun sperta
	  E-parolanto estas bona rimedo por disvolvi viajn sciojn. La interreto
	  estas la ideala rimedo por korespondi mond-skale. Cetere ni donos
	  &#265;i-sube adreson de interreta E-Koresponda Servo. Beda&#365;rinde la
	  kompetentuloj ofte estas tro okupataj kaj ne povas oferi tempon por
	  korespondado kun komencanto. Tamen korespondi kun alilanda
	  komencanto povas esti interesa sperto, sed memoru nian trian
	  konsilon&nbsp;!</p>
	
	  <p>Kvina kaj lasta konsilo : <strong>instruu&nbsp;!</strong> Jes, de kiam vi havos iom pli da
	  sperto en la parola lingvo, vi i&#285;os tre bona korektanto por niaj
	  kursoj, eventuale sub la kontrolo de pli sperta homo. Komence de la
	  ekzisto de Esperanto multaj elstaraj instruistoj komencis instrui tuj
	  post kelksemajna lernado. Oni povas havi dubojn pri la efikeco de
	  tiaj lernmetodoj, sed estu certa, ke post via tri- a&#365; kvarmonata
	  studado anka&#365; vi plene kapablas instrui al komencanto.</p>
	
