	<h3 id="avant-propos">Avant-propos</h3>

	<p>On ne connaît pas vraiment l’espéranto si l’on n’est pas capable de tirer parti des potentialités présentes dans les bases minimales du vocabulaire et de la grammaire. L’élève apprend un nombre très restreint de nouvelles racines dans chaque chapitre de <i>Gerda malaperis!</i> mais leur potentiel expressif est immense. C’est donc l’un des buts de ce recueil que de l’aider à découvrir cette richesse.</p>
	<p>Pour atteindre ce but il est conseillé à l’élève qui vient d’étudier un chapitre de <i>Gerda malaperis!</i> de lire immédiatement le texte de même numéro de ce recueil. Il connaîtra tout ce qui est nécessaire, au niveau grammatical et lexical, pour comprendre ce texte. Par exemple, s’il a appris la matière des chapitres 1 à 7 inclus, il ne rencontrera dans le texte numéro 7 rien qu’il n’ait déjà étudié antérieurement. Ou plutôt il n’y rencontrera aucune nouveauté grammaticale ni aucune racine nouvelle mais il sera confronté à des mots nouveaux. La nécessité d’en deviner le sens, sur la base des éléments déjà acquis, l’initiera progressivement au système de dérivation et de 
	composition de l’espéranto.</p>
	<p>Affermir les connaissances acquises, tel est le second but de ce recueil. La totalité du matériau linguistique de <i>Gerda malaperis!</i> y est réutilisée et les racines qui n’apparaissent pas assez souvent dans le roman y sont répétées suffisamment pour qu’elles se fixent dans la mémoire de l’élève avec un minimum d’effort de sa part. L’effet de fixation se trouvera ici renforcé par le fait que les éléments en question sont employés dans des textes dont l’atmosphère et les thèmes sont tout autres que dans le roman.</p>
	<p>Ce recueil se compose pour l’essentiel de monologues. L’élève peut ainsi s’exercer seul&nbsp;: en lisant le texte à haute voix il utilisera une langue parlée authentique et il pourra s’identifier au personnage tout comme le ferait un acteur. Ce type d’identification est un des ressorts psychologiques connus pour favoriser l’assimilation d’une langue.</p>
	<p>Entre les numéros 13 et 24, le recueil présente deux textes en parallèle à chaque chapitre de <i>Gerda malaperis!</i>. L’élève aura loisir de choisir le morceau qui lui plaît le mieux au premier coup d’œil, à moins qu’il ne veuille lire les deux.</p>
	<p>Si, grâce à ces exemples d’espéranto parlé, rédigés avec un vocabulaire limité, l’élève arrive à prendre conscience des possibilités multiples et diverses que lui offrent ces éléments minimaux de la langue pour s’exprimer, alors le but principal de cet ouvrage aura été atteint.</p>

	<p>Claude Piron (traduction&nbsp;MB)</p>

	<h3 id="antauxparolo">Préambule</h3>

	<p>Oni ne vere posedas Esperanton, se oni ne kapablasekspluati la multajn eblecojn, kiujn jam entenas minimuma vorta kaj gramatika scio. Nur malmultajn radikojn la lernanto ekkonas en ĉiu 	unuopa ĉapitro de <i>Gerda malaperis !</i>, sed ilia latenta esprimpovo 	estas grandega. Helpi lin malkovri tiun riĉecon estas unu el la celoj de ĉi tiu kolekto.</p>
	<p>Por tion atingi, la lernanto saĝe agos, se, studinte 	ĉapitron de <i>Gerda malaperis !</i>, li tuj legos la samnumeran tekston en ĉi tiu kolekto. Li jam konos ĉion necesan, vorte kaj gramatike, por kompreni ĝin. Ekzemple, se li lernis la materialon de <i>Gerda malaperis !</i> ĝis, inkluzive, ĉapitro 7, li trovos sub la numero 7 de ĉi tiu kolekto tekston, en kiu nenio nelernita prezentiĝos. Aŭ, pli ĝuste, li renkontos en tiu teksto neniun novan gramatikaĵon kaj neniun novan <b>radikon</b>, sed jes novajn <b>vortojn</b>, kaj la neceso diveni ilian sencon surbaze de la elementoj, kiujn li lernis, iom post iom enkondukos lin en la vortfaran sistemon de Esperanto.</p>
	<p>Dua celo de ĉi tiu kolekto estas firmigi la 	akiritajn sciojn. La tuta lingva materialo de <i>Gerda malaperis !</i>, estas ĉi tie reuzata, kaj tiuj radikoj, kiuj aperis tro malofte en la romaneto, estas daŭre ripetataj en ĉi tiuj tekstoj, tiel ke ili firme fiksiĝos en la memoro de la lernanto kun peno minimuma. Al la firmiga efiko kontribuos ankaŭ la fakto, ke la koncernaj elementoj 	troviĝas ĉi tie en tekstoj kun tute alia atmosfero, pritraktantaj 	tute aliajn temojn.</p>
	<p>Ĉi tiu kolekto konsistas plejparte el monologoj. Tio 	ebligas al la lernanto ekzerci sin sola: legante tekston laŭtvoĉe, 	li uzos vere parolan lingvon, kaj povos identigi sin al la koncerna rolulo, kiel aktoro faras. Tia identigo estas unu el la konataj 	psikologiaj rimedoj favori la asimiladon de lingvo.</p>
	<p>De numero 13 ĝis numero 24, inkluzive, du malsamaj tekstoj prezentiĝas por ĉiu paralela ĉapitro de <i>Gerda malaperis !</i>. La lernanto tiel povos elekti la pecon, kiu unuarigarde pli plaĉas al li, se li ne volos legi ambaŭ.</p>
	<p>Se, dank' al ĉi tiuj ekzemploj de parola Esperanto, verkitaj kun malgranda vortprovizo, la lernanto konsciiĝos, kiel multe kaj diverse minimumaj lingvo-elementoj ebligas esprimi sin, la 	ĉefa celo de ĉi tiu verko estos atingita.</p>
	<p>Claude Piron</p>
	
