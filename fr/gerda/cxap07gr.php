		<p>Note: Au pluriel, la terminaison <em>-n</em> est ajout&eacute;e &agrave; la terminaison <em>-j</em> :</p>
			<p><span class='ekz'>mi konas belajn knabinojn</span> : je connais des belles filles</p>
