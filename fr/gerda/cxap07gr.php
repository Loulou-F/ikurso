		<br>
		<p>En plus de montrer le compl&eacute;ment d'objet direct, la terminaison <em>-n</em>
		sert aussi &agrave; montrer le mouvement (compl&eacute;ment de lieu avec mouvement).
		Toutefois si le mouvement est d&eacute;j&agrave; implicitement indiqu&eacute; par la
		pr&eacute;position qui introduit le compl&eacute;ment (par exemple gr&acirc;ce &agrave; une
		pr&eacute;position <em>al</em> ou <em>&#285;is</em>) alors la terminaison <em>-n</em> ferait double
		emploi et on ne l'utilise pas.</p>

		<p><span class='ekz'>li iris en la &#285;ardenon</span> : il est entr&eacute; dans le jardin</p>
		<p><span class='ekz'>li iris al la &#285;ardeno</span> : il est all&eacute; jusqu'au jardin</p>

		<p><span class='ekz'>li foriras Parizon</span> : il part &agrave; Paris</p>
		<p><span class='ekz'>li foriras al Parizo</span> : il part &agrave; Paris</p>
		<br>
		<p>La terminaison <em>-n</em> n'est pas utilis&eacute;e uniquement pour indiquer le
		compl&eacute;ment d'objet direct, ou la direction d'un mouvement, mais aussi
		dans l'expression du moment, de la dur&eacute;e, de la mesure, de la valeur,
		quand il n'y a pas de pr&eacute;position:</p>

		<p><span class='ekz'>mi restis la tutan semajnon</span> : je suis rest&eacute; toute la semaine</p>
		<br>
		<p>Dans ce cas la terminaison <em>-n</em> remplace la pr&eacute;position qui est alors
		sous-entendue ; ainsi, si vous utilisez une pr&eacute;position, vous ne devez
		pas utiliser la terminaison <em>-n</em>, et vous pouvez dans ce cas exprimer la
		m&ecirc;me id&eacute;e avec la phrase: </p>

