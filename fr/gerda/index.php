				<a id="prononciation"></a>
				<h2>Rappel sur la prononciation</h2>
			
				<p class="parto">
				Ce bref exposé est destiné à ceux qui veulent commencer l’étude de
				l’espéranto directement avec le cours <em>Gerda malaperis</em>. Ils
				trouveront ici résumé tout ce qu’il faut savoir sur la prononciation de
				l’espéranto. Si cette introduction vous paraît trop technique n’hésitez
				pas à demander des éclaircissements à votre tuteur. Par ailleurs la
				prononciation est indiquée dans les listes de vocabulaire des
				premières leçons.</p>
			
				<p class="parto">L’<b>alphabet</b> espéranto comporte 28 lettres :</p>
				<div class="container row">
						<p class="col s12 row" style="font-size: 1.5em"><span class="col s1">&nbsp;A&nbsp;</span><span class="col s1">&nbsp;B&nbsp;</span><span class="col s1">&nbsp;C&nbsp;</span><span class="col s1">&nbsp;Ĉ&nbsp;</span><span class="col s1">&nbsp;D&nbsp;</span><span class="col s1">&nbsp;E&nbsp;</span><span class="col s1">&nbsp;F</span></p>
						<p class="col s12 row" style="font-size: 1.5em"><span class="col s1">&nbsp;G&nbsp;</span><span class="col s1">&nbsp;Ĝ&nbsp;</span><span class="col s1">&nbsp;H&nbsp;</span><span class="col s1">&nbsp;Ĥ&nbsp;</span><span class="col s1">&nbsp;I&nbsp;</span><span class="col s1">&nbsp;J&nbsp;</span><span class="col s1">&nbsp;Ĵ</span></p>
						<p class="col s12 row" style="font-size: 1.5em"><span class="col s1">&nbsp;K&nbsp;</span><span class="col s1">&nbsp;L&nbsp;</span><span class="col s1">&nbsp;M&nbsp;</span><span class="col s1">&nbsp;N&nbsp;</span><span class="col s1">&nbsp;O&nbsp;</span><span class="col s1">&nbsp;P&nbsp;</span><span class="col s1">&nbsp;R</span></p>
						<p class="col s12 row" style="font-size: 1.5em"><span class="col s1">&nbsp;S&nbsp;</span><span class="col s1">&nbsp;Ŝ&nbsp;</span><span class="col s1">&nbsp;T&nbsp;</span><span class="col s1">&nbsp;U&nbsp;</span><span class="col s1">&nbsp;Ŭ&nbsp;</span><span class="col s1">&nbsp;V&nbsp;</span><span class="col s1">&nbsp;Z</span></p>
				</div>
	
				<p class="parto">Dans les exercices, il suffit de taper la lettre suivie d’un x,
				par exemple&nbsp;: <b>cx</b>, <b>sx</b>, <b>ux</b>… pour obtenir automatiquement la lettre accentuée correspondante.</p>

				<div class="row">
					<p class="col s2"><b>cx</b> → ĉ</p> 
					<p class="col s2"><b>gx</b> → ĝ</p> 
					<p class="col s2"><b>hx</b> → ĥ</p> 
					<p class="col s2"><b>jx</b> → ĵ</p> 
					<p class="col s2"><b>sx</b> → ŝ</p> 
					<p class="col s2"><b>ux</b> → ŝ</p> 
				</div>

				<p class="parto">Les <b>principes de la prononciation</b> sont très simples et sans exception :</p>
				<ol>
					<li>un son (phonème) unique pour chaque lettre et réciproquement</li>
					<li>une syllabe pour chaque voyelle et réciproquement</li>
					<li>l’accent tonique est sur l’avant-dernière syllabe</li>
				</ol>
				
				<p class="rimarko"><strong>REMARQUE</strong><br>
				La syllabe qui porte l’accent  est prononcée avec plus de
				force que les autres syllabes du mot. On dit que cette syllabe est
				«tonique», ou encore «accentuée» (aucun rapport avec l’accent sur les
				lettres ĉ, ĝ etc.). En français l’accent tonique porte sur la dernière
				syllabe du mot&nbsp;: si vous comparez le mot français "ami" et le mot
				espéranto <em>amiko</em>, vous verrez que dans les deux langues l’accent
				tonique porte sur la syllabe <b>mi</b>. Pour prononcer correctement le
				mot <em>amiko</em> dites le mot français «ami» puis ajoutez la syllabe
				<em>ko</em>&nbsp;: <span class="eo">a<u>mi</u>ko</span>.
				</p>
	
