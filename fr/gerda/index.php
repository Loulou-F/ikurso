				<p class="rimarko"><strong>REMARQUE</strong> : </p>
				<ol>
					<li>Il n’y a que très peu de racines en espéranto qui aient une consonne
					double. Un exemple est <span class="eo">Finno</span> (Finnois). Comme chaque lettre se
					prononce, on dit <span class="eo"><u>fin</u>-no</span>. Certains mots composés peuvent avoir
					une consonne double, par exemple <span class="eo">Pollando</span> (Pologne) de <span class="eo">Polo</span>,
					<span class="eo">lando</span>, et on prononce <span class="eo">Pol-<u>lan</u>-do</span>.</li>
					
					<li>Les consonnes sont peu influencées par le contexte. Les consonnes
					sonores en finale ne s’assourdissent pas, pas plus qu’au contact d’une
					consonne sourde&nbsp;: <span class="eo">abstrakta</span> se prononce <span class="eo">ab<u>strak</u>ta</span> plutôt que
					<span class="eo">ap<u>strak</u>ta</span>. Les sourdes ne deviennent pas sonores&nbsp;: dans <span class="eo">kapitalismo</span>
					le <b>s</b> ne se prononce pas comme un <b>z</b>.</li>
				</ol>
				
				<p>c) <b>Diphtongues</b></p>
				<p>
				   Les consonnes <em>j</em> et <em>ŭ</em> sont appelées semi-voyelles du fait de leur
				   proximité avec les voyelles <em>i</em> et <em>u</em>. Les combinaisons suivantes de
				   voyelles (a, e, o, u) et de semi-voyelle <em>j</em> ou <em>ŭ</em> forment un
				   son complexe qu’on appelle traditionnellement «diphtongue». 
				</p>
