<h3 id="lec05.3">5.2. Les corrélatifs</h3>

<p>
Les corrélatifs forment un tableau très utile de petits mots reliés
entre eux par le sens et par la forme. Ils constituent un bon exemple
des avantages que l’on trouve à construire des mots de façon
<b>systématique</b>. 
</p>
<p>
Remarquez que, dans un tableau rectangulaire de 9 lignes et 5
colonnes, l’on peut identifier 45 cases différentes en utilisant
simplement 5 lettres au début et 9 chiffres à la fin : A1, A2, C3,....
H9. De la même façon, il existe en espéranto 45 mots correspondants à
toutes les combinaisons possibles par concaténation de 5 débuts de
mots et de 9 terminaisons. Ces mots sont appelés <b>corrélatifs</b>.
Nous avons rempli quelques cases. à vous de compléter le tableau. La
signification exacte des mots trouvés vous sera expliquée ensuite.
</p>

<p class="meze">
<table>
	<tr>
		<td width="150">&nbsp;</td>
		<td width="150"><b>ki-</b></td>
		<td width="150"><b>ti-</b></td>
		<td width="150"><b>i-</b></td>
		<td width="150"><b>&#265;i-</b></td>
		<td width="150"><b>neni-</b></td>
	</tr>
	<tr><td><b>-o</b></td><td>...</td><td>...</td><td>...</td><td>...</td><td>nenio</td></tr>
	<tr><td><b>-a</b></td><td>kia</td><td>...</td><td>...</td><td>...</td><td>...</td></tr>
	<tr><td><b>-e</b></td><td>...</td><td>...</td><td>ie</td><td>...</td><td>...</td></tr>
	<tr><td><b>-u </b></td><td>...</td><td>tiu</td><td>...</td><td>...</td><td>...</td></tr>
	<tr><td><b>-om</b></td><td>...</td><td>...</td><td>...</td><td>&#265;iom</td><td>...</td></tr>
	<tr><td><b>-am</b></td><td>...</td><td>...</td><td>iam</td><td>...</td><td>...</td></tr>
	<tr><td><b>-el</b></td><td>...</td><td>...</td><td>...</td><td>...</td><td>neniel</td></tr>
	<tr><td><b>-al</b></td><td>kial</td><td>...</td><td>...</td><td>...</td><td>...</td></tr>
	<tr><td><b>-es</b></td><td>...</td><td>ties</td><td>...</td><td>...</td><td>...</td></tr>
</table>
</p>

<p>
Le sens de tout corrélatif est la combinaison des sens des deux
parties qui le composent comme indiqué dans le schéma suivant.
(Insérez la partie gauche à la place du "___" de la partie droite.)
</p>
<a id="cg032"></a><a id="cg033"></a><a id="cg034"></a>
<p>
<pre>
                   +===========+
        quel(le),  | KI      O |  ___ chose (en général)
 lequel, laquelle  |           |
                   |         A |  ___ sorte de, espèce de, genre de
   ce, cet, cette  | TI        |
                   |         U |  ___ personne (voir note [1])
                   |           |
 un(e) certain(e)  | I      ES |  de ___ personne (voir note [2]) 
                   |           |
                   |        OM |  en ___ quantité (voir note [3])
          tout(e)  | &#264;I       |
                   |        AM |  à ___ temps, moment
                   |           |
(ne) ... aucun(e)  | NENI   AL |  pour ___ raison, cause, motif
                   |           |
                   |        EL |  de ___ manière, façon
                   |           |
                   |         E |  à ___ lieu, endroit
                   +===========+
</pre>
</p>

<p>Exemples :</p>
<table class="retrait">
<tr>
	<td width="100"><b>Corrélatif</b></td>
	<td width="100"><b>Composition</b></td>
	<td width="250"><b>Signification</b></td></tr>
<tr><td class="eo">&#264;iam</td><td class="ekz">&#265;I- + -AM</td><td class="fr">"tout(e)" + "à ___ moment"<br/>"à tout moment" => toujours</td></tr>
<tr><td class="eo">nenial</td><td class="ekz">NENI- + -AL</td><td class="fr">"pour ___ raison" + "(ne) ... aucun(e)"<br/>pour aucune raison</td></tr>
<tr><td class="eo">tie</td><td class="ekz">TI- + -E</td><td class="fr">"à ___ lieu" + "ce, cet(te)"<br/>à ce lieu => ici, là</td></tr>
</table></p>

<p><b>Notes</b> :</p>
<ol>
<p>
<li> Un corrélatif en "-U" peut avoir trois valeurs&#160;:</li>
</p>
<p>
   <ul>
   <li> Le plus souvent c’est un <b>adjectif</b> accompagnant un nom&#160;:</li>
   <table>
		<tr>
			<td width="200" class="ekz">Kiun frukton vi volas&#160;?</td>
			<td width="20">&nbsp;</td>
			<td width="200" class="fr">Quel fruit veux-tu&#160;?</td>
		</tr>
		<tr>
			<td class="ekz">Mi volas tiun frukton.</td>
			<td>&nbsp;</td>
			<td class="fr">Je veux ce fruit(-là).</td>
		</tr>
		<tr>
			<td class="ekz">Mi volas neniun frukton.</td>
			<td>&nbsp;</td>
			<td class="fr">Je ne veux aucun fruit.</td>
		</tr>
		<tr>
			<td class="ekz">Kiun amikon vi renkontis&#160;?</td>
			<td>&nbsp;</td>
			<td class="fr">Quel ami avez-vous rencontré&#160;?</td>
		</tr>
</table>
</p>
<p>
   <li> et le nom peut parfois &ecirc;tre sous-entendu si le contexte est clair&#160;:</li>
		<table>
			<tr>
				<td width="250" class="ekz">Jen fruktoj : kiun (frukton) vi volas&#160;?</td>
				<td width="20">&nbsp;</td>
				<td width="250" class="fr">Voici des fruits : lequel veux-tu&#160;? </td>
			</tr>
			<tr>
				<td class="ekz">- Mi volas tiun (frukton).</td>
				<td>&nbsp;</td>
				<td class="fr">- Je veux celui-là.</td>
			</tr>
			<tr>
				<td class="ekz">- Mi volas neniun (frukton).</td>
				<td>&nbsp;</td>
				<td class="fr">- Je n’en veux aucun.</td>
			</tr>
			<tr>
				<td class="ekz">Jen la frukto, kiun (frukton) vi volis.</td>
				<td>&nbsp;</td>
				<td class="fr">Voici le fruit que tu voulais.</td>
			</tr>
	</table>
</p>
<p>
	<li> mais, employé seul, il s’agit le plus souvent d’un <b>pronom</b> qui désigne une personne&#160;:</li>
		<table>
			<tr>
				<td width="250" class="ekz">Kiu venis&#160;?</td>
				<td width="20">&nbsp;</td>
				<td width="250" class="fr">Qui est venu&#160;?</td>
			</tr>
			<tr>
				<td class="ekz">- Iu.</td>
				<td>&nbsp;</td>
				<td class="fr">- Quelqu’un.</td>
			</tr>
			<tr>
				<td class="ekz">- Neniu.</td>
				<td>&nbsp;</td>
				<td class="fr">- Personne.</td>
			</tr>
			<tr>
				<td class="ekz">- &#265;iu.</td>
				<td>&nbsp;</td>
				<td class="fr">- Chacun.</td>
			</tr>
	</table>
  </ul>
</p>
<p>
<li>Tout corrélatif en "-ES" est associé à un pronom en "-U" et marque
    la possession&#160;:</li>
</p>
<table>
	<tr><td width="100" class="eo">kies (= de kiu) :</td><td width="20">&nbsp;</td><td width="250" class="fr">de qui, à qui, dont, duquel</td></tr>
	<tr><td class="eo">ties (= de tiu) :</td><td>&nbsp;</td><td class="fr">de celui-là, à celui-là</td></tr>
	<tr><td class="eo">ies  (= de iu)  :</td><td>&nbsp;</td><td class="fr">de quelqu’un, à quelqu’un</td></tr>
	<tr><td class="eo">&#265;ies (= de &#265;iu) :</td><td>&nbsp;</td><td class="fr">de chacun, de tout le monde, à tout le monde</td></tr>
	<tr><td class="eo">nenies (= de neniu) :</td><td>&nbsp;</td><td class="fr">de personne, à personne</td></tr>
</table>

<p>Exemple&#160;:</p>
<table>
	<tr><td width="250" class="ekz">Kies amiko vi estas&#160;?</td><td width="250" class="fr">De qui es-tu l’ami&#160;?</td></tr>
	<tr><td class="ekz">Mi estas &#265;ies amiko.</td><td class="fr">Je suis l’ami de tous (de chacun).</td></tr>
	<tr><td class="ekz">La viro, kies amikon mi vidis.</td><td class="fr">L’homme dont j’ai vu l’ami</td></tr>
	<tr><td class="ekz">La viro, kun kies amiko ni trinkis.</td><td class="fr">L’homme avec l’ami duquel nous avons bu</td></tr>
</table>
<p>
<li> Les corrélatifs en "-OM" (quantité) sont souvent suivi par la
    préposition "da"&#160;:</li>
<table>
	<tr><td width="250" class="ekz">Kiom da kukoj vi deziras&#160;?</td><td class="fr">Combien de gâteaux veux-tu&#160;?</td></tr>
	<tr><td class="ekz">Mi jam man&#285;is tiom da ili&#160;!</td><td class="fr">J’en ai déjà tellement mangé&#160;!</td></tr>
</table>
</ol>	 
</p>

<h3 id="lec05.3">5.3. Un peu de pratique</h3>
<p>
Après toute cette théorie, un peu de pratique ne vous fera pas de mal.
Regardez les mots de la liste qui suit. Décomposez chaque corrélatif,
recherchez son sens d’après les explications du paragraphe précédent
et comparez-le avec celui qui est donné ici.
</p>
<table class="retrait">
	<tr><td width="50" class="eo">tio</td><td width="200" class="fr">cette chose, cela, ce</td></tr>
	<tr><td class="eo">kio&#160;?</td><td class="fr">quelle chose&#160;? quoi&#160;?</td></tr>
	<tr><td class="eo">iam</td><td class="fr">à un (certain) moment, un jour, (il était) une fois</td></tr>
	<tr><td class="eo">nenie</td><td class="fr">nulle part</td></tr>
	<tr><td class="eo">kiel&#160;?</td><td class="fr">comment&#160;? de quelle manière&#160;? (réponse: bien, élégamment...)</td></tr>
	<tr><td class="eo">kiam&#160;?</td><td class="fr">quand&#160;? à quel moment&#160;? (réponse: cet après-midi, le soir...)</td></tr>
	<tr><td class="eo">kie&#160;?</td><td class="fr">o&ugrave;&#160;? en quel lieu&#160;? (réponse: tout près, au Canada...)</td></tr>
	<tr><td class="eo">kial&#160;?</td><td class="fr">pourquoi&#160;? pour quelle raison&#160;? (réponse: parce que...)</td></tr>
	<tr><td class="eo">kiom&#160;?</td><td class="fr">combien&#160;? quelle quantité&#160;? (réponse: un peu, beaucoup...) </td></tr>
	<tr><td class="eo">&#265;iam</td><td class="fr">toujours</td></tr>
	<tr><td class="eo">tiel</td><td class="fr">ainsi, de cette façon</td></tr>
	<tr><td class="eo">ie</td><td class="fr">quelque part</td></tr>
</table>

<div class="rim"><p><b>Remarque</b> : </p>
<p>
	<ol>
		<li>Faites bien attention à l’accent tonique des corrélatifs&#160;:
		ne-<u>ni</u>-e, <u>ki</u>-u, <u>ti</u>-al, <u>i</u>-om, ne-<u>ni</u>-es.  
		<li>Les corrélatifs en "KI-" servent à former des phrases interrogatives
		(qui&#160;?, o&ugrave;&#160;?, quand&#160;?, comment&#160;?) et des propositions relatives.
	</ol>
</p>
</div>

<h3 id="lec05.4">5.4. La flexion des corrélatifs</h3>

<p>
Les corrélatifs terminés par "-A" et "-U" prennent la forme de l’accusatif
<b>-n</b> et du pluriel <b>-j</b>. Les corrélatifs en "-O" ("kio",
etc.) et en "-E" prennent le <b>-n</b> de l’accusatif mais jamais le <b>-j</b> du
pluriel. Tous les autres sont invariables.
</p>
<table class="retrait">
	<tr><td class="ekz">Kun kiaj fruktoj vi faris tiun bonan kukon&#160;?</td><td width="10">&nbsp;</td><td class="fr">Avec quelles sortes de fruits as-tu fait ce bon gâteau&#160;?</td></tr>
	<tr><td class="ekz">Mi neniam man&#285;is tiajn kukojn.</td><td>&nbsp;</td><td class="fr">Je n’ai jamais mangé de tels gâteaux</td></tr>
	<tr><td class="ekz">Kiujn vi renkontis&#160;?</td><td>&nbsp;</td><td class="fr">Qui (plur.) as-tu rencontré&#160;? Lesquels as-tu rencontrés&#160;?</td></tr>
	<tr><td class="ekz">Mi vidis la kukon, kiun vi faris.</td><td>&nbsp;</td><td class="fr">J’ai vu le gâteau que tu as fait</td></tr>
	<tr><td class="ekz">Kion vi volas&#160;?</td><td>&nbsp;</td><td class="fr">Que veux-tu&#160;?</td></tr>
</table>

