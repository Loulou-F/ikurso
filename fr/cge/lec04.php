<h3 id="lec04.1">4.1. La forme interrogative</h3>
<p>
Jetons un coup d’œil sur les <b>questions et réponses</b> correspondant à une
affirmation donnée.
</p>
<table class="retrait">
	<tr><td width="30%">- énoncé :</td><td width="70%" class="ekz-eo">La pano estas bruna.</td></tr>
	<tr><td>&nbsp;</td><td>= Le pain est brun.</td></tr>
	<tr><td>&nbsp;</td></tr>
	<tr><td>- Question :</td><td class="ekz"><b>&#264;u</b> la pano estas bruna&#160;?</td></tr>
	<tr><td>&nbsp;</td><td>= Est-ce que le pain est brun&#160;? (le pain est-il brun&#160;?)</td></tr>
	<tr><td>&nbsp;</td></tr>
	<tr><td>- Réponse :</td><td class="ekz"><b>Jes</b>, la pano estas bruna.</td></tr>
	<tr><td>&nbsp;</td><td>= Oui, le pain est brun.</td></tr>
	<tr><td>&nbsp;</td><td>_________   ou   ___________ </td></tr>
	<tr><td>&nbsp;</td><td class="ekz"><b>Ne</b>, la pano ne estas bruna, sed (&#285;i estas) blanka.</td></tr>
	<tr><td>&nbsp;</td><td>= Non, le pain n’est pas brun mais (il est) blanc.</td></tr>
</table>
<p>
Vous aurez remarqué que <i>ne</i> sert aussi bien en début de phrase que devant
le verbe. <i>Jes</i> signifie <i>oui</i> et <i>sed</i> signifie <i>mais</i>.
</p>
<p>
Toute question est construite à partir d’une affirmation. On forme la
question en plaçant devant l’énoncé le mot interrogatif <i>&#265;u</i> (est-ce
que&#160;?). Remarquez qu’en espéranto (tout comme en français quand on emploie
<i>est-ce que&#160;?</i>), on ne modifie pas l’ordre des mots.  On place donc
simplement le mot <em>&#265;u</em> au début de la phrase.
</p>
<table class="retrait">
	<tr><td>Question :</td><td>Est-ce que les garçons vendront le g&acirc;teau&#160;?</td></tr>
	<tr><td>Affirmation sous-jacente :</td><td>Les garçons vendront le g&acirc;teau.</td></tr>
	<tr><td>&nbsp;</td><td class="ekz">La knaboj vendos la kukon.</td></tr>
	<tr><td>Question en espéranto :</td><td class="ekz">&#264;u la knaboj vendos la kukon&#160;?</td></tr>
</table>
<p>
Fonctionnent de cette façon toutes les questions auxquelles on doit
répondre par <i>oui</i> ou par <i>non</i> (questions &#171;fermées&#187;).
</p>

