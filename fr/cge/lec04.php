<h3 id="lec04.2">4.2. L’alphabet (rappel)</h3>

<p>L’<b>alphabet</b> espéranto comporte 28 lettres :</p>
<p class="meze"><img alt="Alphabet" src="alfabeto.gif"></p>
<p>
Les lettres <em>q</em>, <em>w</em>, <em>x</em> et <em>y</em> n’existent pas en
espéranto mais on y trouve six lettres qui n’existent pas en français&nbsp;:
<em>&#265;</em>, <em>&#285;</em>, <em>&#293;</em>, <em>&#309;</em>, <em>&#349;</em>, <em>&#365;</em> (à
remarquer qu’elles ont toutes un accent circonflexe sauf le <em>&#365;</em> dont
l’accent est en fait un demi-cercle).
</p>
<p>
Quand on a besoin d’épeler un mot en espéranto on ne dit pas <i>a</i>,
<i>bé</i>, <i>cé</i>,... <i>ache</i>,... <i>zèd</i> comme en français mais
<i>a</i>, <i>bo</i>, <i>co</i>, <i>do</i>, <i>e</i>, etc., c’est-à-dire que
le nom des voyelles correspond à leur son et que le nom des consonnes se
forme en leur ajoutant la terminaison <b>-o</b>.
</p>
<p>Exemples :</p>
<table class="retrait">
	<tr><td><span class="ekz">griza</span>   : go, ro, i, zo, a</td></tr>
	<tr><td><span class="ekz">ru&#285;a</span>   : ro, u, &#285;o, a<td></tr>
</table>

<p>Souvenez-vous bien de la règle sans exception : <b>une lettre = un son</b>.</p>

<div class="rim"><p><b>Remarque</b> : 
	Il n’y a que très peu de racines en espéranto qui aient une consonne
	double. Un exemple est <i>Finno</i> (Finnois). Comme chaque lettre se
	prononce, on dit <i><u>fin</u>-no</i>. Certains mots composés peuvent avoir
	une consonne double, par exemple <i>Pollando</i> (Pologne) de <i>Polo</i>,
	<i>lando</i>, et on prononce <i>Pol-<u>lan</u>-do</i>.</p>
</div>

