				  	<h3>Clavier espéranto pour iPhone / iPod Touch / iPad</h3>
				  	
				  	<p>
					Pour taper les caractères accentués propres à l’espéranto sur votre iPhone, iPod Touch ou iPad, nous vous conseillons
					d’installer l’application <a href="https://itunes.apple.com/us/app/esperanta-klavaro/id957192189?mt=8&ign-mpt=uo%3D4">Esperanta Klavaro</a>, disponible gratuitement sur l’AppStore. Cette application a besoin de iOS 8.1 (ou une version plus récente) pour fonctionner.</p>
				  	</p>
				  			  	

				  	<h3>Clavier espéranto pour Android</h3>
				  	
				  	<p>L'application <a href="https://play.google.com/store/apps/details?id=com.menny.android.anysoftkeyboard&hl=fr">AnySoftKeyboard</a> disponible gratuitement sur Google Play, vous permettra de taper l'espéranto directement au clavier.</p>