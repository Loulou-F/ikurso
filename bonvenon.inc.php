					<h3 id="ihm">Présentation du site</h3>
					<p>
					Sur chaque page, deux menus permettent d’accéder à toutes les fonctionnalités du site :<br>
					- le menu du haut permet d’accéder à toutes les fonctionnalités spécifiques à chaque type d’utilisateur. 
					Il n’est affiché dès que l’utilisateur s’est identifié.<br>
					- le menu de gauche permet d’accéder aux cours, aux pages d’aide et d’information. <br>
					- si vous êtes élève du «Cours en dix leçons» ou de «Gerda malaperis», vous verrez dans ce menu un
					lien vous permettant d’aller directement sur la prochaine leçon à étudier.					
					</p>
					
					<h3 id="onglets">Pages de cours</h3>
					<p>
					Le système d’onglets permet de passer facilement d’une page à l’autre, rendant plus pratique la
					navigation à l’intérieur du cours. Dans le cours <a href="fr/gerda/">Gerda malaperis</a>, ce système
					permet un découpage des leçons en différentes étapes. Ainsi il est facile de passer du
					texte aux exercices en passant par la partie grammaire et vocabulaire. 					
					</p>
					
					<h3 id="accents">Accents</h3>
					<p>Les accents posent parfois problème, notamment dans l’envoi des messages. On a alors souvent
					recours à la méthode en -x ou en -h. Cependant, dans la majorité des cas, il est tout à fait possible 
					d’<a href="helpo.php?temo=faq">utiliser les accents dans les 
					messages</a> et ce qui était difficile à mettre en oeuvre il y a dix ans ne l’est plus aujourd’hui. 
					<br>
					Vous trouverez dans les pages d’aide toutes les informations vous permettant de le faire facilement.
					</p>
					
					
