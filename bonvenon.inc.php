					<h3 id="accents">Accents</h3>
					<p>Les accents posent parfois problème, notamment dans l’envoi des messages. On a alors souvent
					recours à la méthode en -x ou en -h. Cependant, dans la majorité des cas, il est tout à fait possible 
					d’<a href="helpo.php?temo=faq">utiliser les accents dans les 
					messages</a> et ce qui était difficile à mettre en oeuvre il y a dix ans ne l’est plus aujourd’hui. 
					<br>
					Vous trouverez dans les pages d’aide toutes les informations vous permettant de le faire facilement.
					</p>
					
					<p class="important">Ce qui suit concerne plus particulièrement les élèves du «Cours en dix leçons»
					et du cours «Gerda malaperis». Les utilisateurs du logiciel Kurso de Esperanto pourront cependant y
					trouver des informations utiles concernant l’envoi des messages avec les lettres accentuées.</p>
					
					<h4>Utilisation des accents dans le cours</h4>
					<p>
					Comme vous pourrez le constater, les textes en espéranto sont écrits avec les accents. 
					Cela ne pose généralement aucun problème, mais si cela était le cas, il est possible
					de visualiser les textes avec la méthode "en -X".
					Il suffit pour cela de cliquer sur un des deux petits boutons se trouvant en haut à gauche de l’écran.
					</p>
					<h4>Utilisation des accents dans les messages</h3>
					<p>
					Quel que soit le système utilisé pour l’affichage des pages, il est possible d’utiliser deux systèmes
					pour l'envoi des messages : soit avec le système en -X (qui était utilisée jusqu’à présent pour les cours
					«Cours en dix leçons» et «Gerda malaperis») soit avec les lettres accentuées.<br>
					La plupart des systèmes permettent aujourd'hui d'utiliser les accents dans les messages. C’est pourquoi
					<strong>nous vous conseillons d’utiliser cette dernière méthode</strong>.
					</p>
