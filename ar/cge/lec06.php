	<tr><td class="eo">&#285;is</td><td class="fr">- jusqu’à (est également conjonction et signifie alors: jusqu’à ce que)</td></tr>
	<tr><td class="eo">por</td><td class="fr">- pour, à l’intention de</td></tr>
