				  	<h3 id="accents">Les lettres accentuées : polices de caractères et affichage à l'écran</h3>
				  	
			  			  	
				  	<h4 id="methodes">Les différentes méthodes pour représenter les accents</h4>
				  	<p>
					Lorsque les lettres accentuées ne sont pas disponibles, on peut avoir recours à plusieurs méthodes pour 
					représenter les accents :</p>
					
					<p><strong>- la méthode en «h»</strong> consiste à remplacer l'accent par une lettre «h» placée
					après la lettre concernée, et de laisser le «au» ou le «eu» sans accent.<br>
					C'est la première méthode à avoir été proposée par Zamenhof lui-même, en annexe du 
					«Dua libro», une de ses premières brochures.<br>
				  	</p>
					
					<p><strong>- la méthode en «x»</strong> consiste à remplacer l'accent par une lettre «x» 
					placée après la lettre concernée.</p>
			  		
					<p>
					Le choix entre l'une ou l'autre de ces deux méthodes de substitution est souvent une question
					d'habitude. Certains préfèrent utiliser la méthode en «h» car elle est conforme aux prescriptions
					de Zamenhof (bien que ce point ne soit pas décrit dans le Fundamento,
					mais dans une brochure parue ultérieurement, la «Dua libro»). 
					</p>
					<p>La méthode en «x» est cependant devenue très populaire avec la diffusion de l'informatique et 
					d'internet car la lettre «x» ne faisant pas partie de l'alphabet de l'espéranto, cette lettre est plus adaptée 
					aux remplacements automatiques.<br>
					</p>	
			  		
