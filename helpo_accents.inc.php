				  	<h2>Les lettres accentuées</h2>
				  	
			  			  	
				  	<h3 id="methodes">Les différentes méthodes pour représenter les accents</h3>
				  	<p>
					Lorsque les lettres accentuées ne sont pas disponibles, on peut avoir recours à plusieurs méthodes pour 
					représenter les accents :</p>
					
					<ul class="listo">
						<li>la méthode en «h»</strong> consiste à remplacer l'accent par une lettre «h» placée
					après la lettre concernée, et de laisser le «au» ou le «eu» sans accent.<br>
					C'est la première méthode à avoir été proposée par Zamenhof lui-même, en annexe du 
					<em>Dua libro</em>, une de ses premières brochures.</li>
					
						<li>la méthode en «x»</strong> consiste à remplacer l'accent par une lettre «x» 
					placée après la lettre concernée.</li>
					</ul>
			  		
					<p>
					Le choix entre l'une ou l'autre de ces deux méthodes de substitution est souvent une question
					d'habitude. Certains préfèrent utiliser la méthode en «h» car elle est conforme aux prescriptions
					de Zamenhof (bien que ce point ne soit pas décrit dans le <em>Fundamento</em>,
					mais dans une brochure parue ultérieurement, la <em>Dua libro</em>). 
					</p>
					<p>La méthode en «x» est cependant devenue très populaire avec la diffusion de l'informatique et 
					d'internet car la lettre «x» ne faisant pas partie de l'alphabet de l'espéranto, cette lettre est plus adaptée 
					aux remplacements automatiques.<br>
					</p>	
			  		
				  	<p>Voir aussi l'article de Wikipédia&nbsp;: <a href="http://fr.wikipedia.org/wiki/Alphabet_de_l%27esp%C3%A9ranto" class="btn-flat small blue-text"><i class="material-icons right">arrow_right</i>Alphabet de l’espéranto</a></p>
				  				  	 
					<p>Si vous connaissez déjà un peu l'espéranto, vous trouverez des informations utiles sur le site de Bertilo :  
					<a href="http://www.bertilow.com/komputo/index.html" class="btn-flat small blue-text"><i class="material-icons right">arrow_right</i>Esperanto en la komputilo</a>.</p>
					

					<h3 id="klavaro">Comment taper les lettres accentuées au clavier</h3>
					
					<p>Il existe différents outils permettant de taper les lettres accentuées au clavier. Nous en listons ici
					quelques-uns parmi les plus répandus.</p>
					
					<ul class="collapsible" data-collapsible="accordion">
						<li>
							<div class="collapsible-header"><i class="material-icons primaire-texte texte-clair">keyboard_arrow_down</i>Clavier français-espéranto pour Windows</div>
							<div class="collapsible-body">
								<p>Le contenu de cette page provient du site <a href="http://ttt.jubilo.ca/ttt/clavier/">
									http://ttt.jubilo.ca/ttt/clavier/</a>. L’auteur indique que l’outil fonctionne sous Windows 2000, XP et Server 2003. 
									Il est cependant également possible de le faire fonctionner sous Windows Vista édition familiale.</p>
				
								<p><strong>Exemples :</strong></p>
							    <ul class="listo">
									<li>Téléchargez ce petit 
									<a href="http://ikurso.esperanto-france.org/klavaro.zip">
									fichier ZIP</a>. 
									Il contient le programme d’installation et un fichier DLL (FrEo.dll dans le dossier i386)&nbsp;:<br /><br />
									<img src="http://ikurso.esperanto-france.org/bildoj/klavaro.jpg" class="responsive-img"></li>
									<li>Double-cliquez sur le programme d’installation (*.msi)</li>
									<li>L’installation est terminée !<br>
									<i>Il est bien sûr possible de désinstaller cette configuration.</i></li>
							    </ul>
								<p><strong>Exemples :</strong></p>
							    <ul class="listo">
									<li>Allez dans le <i>Panneau de configuration</i></li>
									<li>Ouvrez le dossier <i>Options régionales et linguistiques</i> (<i>Regional Settings</i>)</li>
									<li>Dans l’onglet <i>Langues</i>, cliquez sur <i>Détails</i>, puis sur <i>Ajouter</i> : choisissez <b><i>Français (France)</i></b> 
									et la configuration <b><i>Français (+Esperanto)</i></b></li>
									<li>La configuration est terminée.</li>
							    </ul>
			    
								<p><strong>Exemples :</strong></p>
								<ul class="listo">
									<li><strong>Pour les lettres avec un accent circonflexe </strong>, comme le 
									<strong>ĉ</strong> : tapez <em>AltGr+c</em>, <em>AltGr+g</em>, <em>
									AltGr+h</em>, <em>AltGr+j</em>, et <em>AltGr+s</em>. Pour les majuscules 
									tapez <em>Shift+AltGr+Lettre</em></li>
									<li><strong>Pour les lettres surmontées d’une brève</strong>, comme le <strong>ŭ</strong> : 
									tapez <em>AltGr+u</em>. Pour les majuscules tapez <em>Shift+AltGr+u</em></li>
									<li>Le fonctionnement des autres touches n’est pas modifié.</li>
								</ul>
								<p><strong>Exemples :</strong></p>
								<ul class="listo">
									<li><strong>ĉevalo</strong><strong>: </strong>
									<em>AltGr-shift-c</em> <strong>e v a l o</strong></li>
									<li><strong>manĝi</strong> :
									<strong>m a n </strong><em>AltGr-g</em> <strong>i</strong></li>
									<li><strong>ankaŭ</strong> : <strong>a n k a</strong> <em>AltGr-u</em></li>
								</ul>
							</div>
						</li>
						<li>
							<div class="collapsible-header"><i class="material-icons primaire-texte texte-clair">keyboard_arrow_down</i>Clavier diacrité pour Windows</div>
