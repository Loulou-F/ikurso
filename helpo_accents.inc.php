				  	<h4>Les polices de caractères permettant d'afficher l'espéranto</h3>

				  	<p><u><strong>Si vous utilisez Windows :</u></strong></p>
				  	
				  	<p>Les versions récentes de Windows (depuis Windows XP) contiennent déjà toutes les polices nécessaires. 
				  	C'est le cas, entre autres, de Arial, Bookman Old Style, Garamond, Georgia, Tahoma, Times New Roman, Verdana,
				  	Courrier New. La liste n'est pas exhaustives et d'autres polices, installées avec Microsoft Office depuis l'édition 2007,
				  	Windows Vista, Windows 7 et les suivantes, permettent aussi d'afficher les caractères accentuées en espéranto.</p>
				  	 
					<p>Le choix de la police utilisée par défaut par le navigateur Internet a également une grande importance. 
					Dans les menus des préférences de votre navigateur Internet, il est possible de choisir entre diverses 
					polices. Les polices précédemment cités sont adéquates comme police par défaut, de même que «Courrier New» 
					comme police à taille fixe.</p>
					
					<p>La police «Verdana» souvent utilisée sur internet car bien lisible à l'écran contient également les 
					caractères utiles pour l'espéranto. C'est cette police qui est utilisée dans ce site pour les textes 
					de nos cours.</p>
					
					<p>Pour savoir si une police contient les caractères souhaités, nous vous conseillons de regarder dans le
					programme «Table des caractères».</p>
					
					<p>En plus des polices installées par Windows, nous pouvons citer deux autres polices, qui ont l'avantage 
					de pouvoir être utilisées librement, d'exister pour tous les systèmes et d'être très complètes :</p>
					<ul>
						<li>Deja Vu : à télécharger sur <a href="http://dejavu-fonts.org">http://dejavu-fonts.org</a> 
						(site en anglais)</li>
						<li>STIX : informations et téléchargements sur 
						<a href="http://www.stixfonts.org/">http://www.stixfonts.org/</a> (site en anglais)</li>
					</ul>
					<p align="right"><em><a href="#top">Revenir au début</a></em></p>

