					<p>MacOS est fourni avec un certain nombre de polices permettant d'afficher correctement les caractères
					accentués de l'espéranto, par exemple «Geneva», «Helvetica», «Arial», «Georgia», «Verdana», «Times New Roman»,
					«Lucida Grande».</p>
					
				
					<p>Les fontes Deja Vu et STIX, citées plus haut, sont également disponibles pour Mac OS.</p>
