				  	<h3 id="courriel">Les lettres accentuées dans le courrier électronique</h3>
				  	<p>
					Dans les cours que nous proposons sur ce site, les messages contenant les exercices des élèves 
					sont envoyés en format HTML. Ce format permet par exemple d'utiliser des couleurs différentes
					pour les questions et les réponses. Il permet aussi de transmettre facilement les caractères
					accentués.</p>
					<p>
					Pour répondre à de tels messages, il faut faire attention à <strong>répondre dans le même format
					</strong>&nbsp;: si vous répondiez à ces messages en utilisant le format «texte brut» (ou «texte seulement»), 						tous 
					les caractères accentués apparaîtraient à votre correspondant sous forme de codes tels 
					que <em>&amp;#265;</em> ou <em>&amp;#264;</em>.</p>

				<h4>Les lettres accentuées avec Mozilla/Thunderbird</h4>
				
						<p>Lorsque vous  répondez à un tel message, vérifiez dans le menu <em>Option</em> que le choix 
						<em>détection automatique</em> ou <em>HTML</em> est coché. Le contenu du message initial ne sera pas alteré
						et les lettres accentuées que vous taperez seront alors correctement transmises à votre
						correspondant. 
					  	<br>
					  	Si vous souhaitez envoyer tous vos messages en HTML : allez dans le menu <em>Outil</em> / <em>Options</em>.
					  	Cliquez sur l'icône <em>Rédaction</em> puis sur le bouton <em>Options d'expédition</em>, choisir 
					  	<em>Envoyer au format HTML</em>.
					  	</p>
					  
				<h4>Les lettres accentuées avec Outlook Express</h4>
						
						<p>La procédure est quasiment la même que pour Thunderbird : 
						lorsque vous répondez à un message en HTML contenant des lettres accentuées, 
						vérifiez dans le menu <em>Format</em> que l'option <em>Texte enrichi (HTML)</em> est cochée.</p>
						<p>
						Attention ! Si vous répondez en utilisant le format <em>texte brut</em>, les caractères accentués 
						ne seront pas transmis mais Outlook ne vous préviendra pas (contrairement à Mozilla).
						<p>
						Si vous voulez que les réponses à vos messages soient toujours envoyées avec le même format que
						le message initial, allez dans le menu <em>Outils</em> / <em>Option</em>, cliquez sur l'onglet 
						<em>Envoi</em> et cochez le choix <em>Réponse aux messages en utilisant le format d'origine</em>.
						<br>
						Si vous souhaitez rédiger tous vos messages en utilisant le format HTML, cochez dans ce même écran
						l'option <em>Format du courrier</em> : <em>HTML</em>.
						</p>
				
				<h4>Les lettres accentuées dans les «webmails»</h4>
				
						<p><strong>Qu'est-ce qu'un «webmail» ?</strong></p>
						
						<p>C'est un outil qui vous permet de recevoir et envoyer votre courrier électronique directement
						depuis une page Internet (sans passer par un logiciel tel que Thunderbird ou Outlook). Exemples : 
						la messagerie disponible sur www.orange.fr ou encore le site www.gmail.com.</p>
						
						<p>Orange et Gmail permettent de recevoir et d'envoyer correctement les messages avec les caractères 
						accentués. Certains autres peuvent ne pas bien prendre en compte les caractères accentués. Nous vous 
						conseillons dans ce cas de faire un essai et de consulter le guide d'utilisation de votre site de
						messagerie pour plus d'information.</p>

