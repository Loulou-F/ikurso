				<h4>Les lettres dans les listes de diffusion</h4>
								
						<p>ATTENTION, lorsque vous envoyez un message à une liste de diffusion (par exemple une liste
						dans yahoogroupes), un forum ou un groupe de news, il est préférable d'écrire en utilisant le système 
						en h ou en x car les systèmes utilisés pour 
						gérer ces groupes ne supportent généralement pas l'Unicode ou le HTML, et le message reçu par vos
						destinataires ferait apparaître des choses pas très lisible en lieu et place de caractères accentués.</p>

