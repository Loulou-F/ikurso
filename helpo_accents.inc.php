					<h3 id="klavaro">Comment taper les lettres accentuées au clavier</h3>
					
					<p>Il existe différents outils permettant de taper les lettres accentuées au clavier. Nous en listons ici
					quelques-uns parmi les plus répandus.</p>
					
					<h4>Clavier français-espéranto pour Windows</h4>
				
					<p class="noto">Le contenu de cette page provient du site <a href="http://ttt.jubilo.ca/ttt/clavier/">
					http://ttt.jubilo.ca/ttt/clavier/</a>. L’auteur indique que l’outil fonctionne sous Windows 2000, XP et Server 2003. 
					Il est cependant également possible de le faire fonctionner sous Windows Vista édition familiale.</p>
				
					<p><strong>Exemples :</strong></p>
				    <ul>
						<li>Téléchargez ce petit 
						<a href="http://ikurso.esperanto-jeunes.org/klavaro.zip">
						fichier ZIP</a>. 
						Il contient le programme d’installation et un fichier DLL (FrEo.dll dans le dossier i386)&nbsp;:<br /><br />
						<img src="http://ikurso.esperanto-jeunes.org/bildoj/klavaro.jpg"></li>
						<li>Double-cliquez sur le programme d’installation (*.msi)</li>
						<li>L’installation est terminée !<br>
						<i>Il est bien sûr possible de désinstaller cette configuration.</i></li>
				    </ul>
					<p><strong>Exemples :</strong></p>
				    <ul>
						<li>Allez dans le <i>Panneau de configuration</i></li>
						<li>Ouvrez le dossier <i>Options régionales et linguistiques</i> (<i>Regional Settings</i>)</li>
						<li>Dans l’onglet <i>Langues</i>, cliquez sur <i>Détails</i>, puis sur <i>Ajouter</i> : choisissez <b><i>Français (France)</i></b> 
						et la configuration <b><i>Français (+Esperanto)</i></b></li>
						<li>La configuration est terminée.</li>
				    </ul>
			    
					<p><strong>Exemples :</strong></p>
					<ul>
						<li><strong>Pour les lettres avec un accent circonflexe </strong>, comme le 
						<strong>ĉ</strong> : tapez <em>AltGr+c</em>, <em>AltGr+g</em>, <em>
						AltGr+h</em>, <em>AltGr+j</em>, et <em>AltGr+s</em>. Pour les majuscules 
						tapez <em>Shift+AltGr+Lettre</em></li>
						<li><strong>Pour les lettres surmontées d’une brève</strong>, comme le <strong>ŭ</strong> : 
						tapez <em>AltGr+u</em>. Pour les majuscules tapez <em>Shift+AltGr+u</em></li>
						<li>Le fonctionnement des autres touches n’est pas modifié.</li>
					</ul>
					<p><strong>Exemples :</strong></p>
					<ul>
						<li><strong>ĉevalo</strong><strong>: </strong>
						<strong><em>AltGr-shift-c</em> <strong>e v a l o</strong></li>
						<li><strong>manĝi</strong> :
						<strong>m a n </strong><em>AltGr-g</em> <strong>i</strong></li>
						<li><strong>ankaŭ</strong> : <strong>
						a n k a</strong> <em>AltGr-u</em></li>
					</ul>	
					
					<h4>Clavier diacrité pour Windows</h4>
				  	
				  	<p>Cet outil est proposé par Michael Langlois. Le <em>clavier diacrité</em> permet de taper directement
				  	au clavier non seulement les caractères accentués de l'espéranto, mais également toutes sortes de
				  	caractères accentués.</p>
				  	
				  	<p>Pour plus d'informations sur l'installation et l'utilisation de ce clavier, voir sur le site de
				  	Michael Langlois : 
				  	<a href="http://michaellanglois.fr/it/french-keyboard-with-diacritics-11-windows_clavier-francais-diacrite-11-windows" class="btn-flat small blue-text"><i class="material-icons right">arrow_right</i>Clavier français diacrité 1.1 Windows</a>.</p>
				  	
					<h4>Clavier diacrité pour MacOS</h4>

				  	<p>Nous vous conseillons d'installer le clavier diacrité de Michael Langlois. 
				  	Une fois installé, il est facile de basculer du clavier français au clavier espéranto. 
				  	Avec le clavier espéranto, il suffit par exemple de taper ^ puis c pour obtenir un ĉ, ou ù 
				  	puis u pour obtenir un ŭ.</p>
				  	
				  	<p>Pour installer et utiliser ce clavier diacrité, voir sur le site de Michael Langlois :
				  	<a href="http://michaellanglois.fr/it/french-keyboard-with-diacritics-11a-mac_clavier-francais-diacrite-11a-mac/lang/fr" class="btn-flat small blue-text"><i class="material-icons right">arrow_right</i>Clavier français diacrité 1.1a Mac</a>.</p>

				  	<h4>Configuration de clavier pour Linux</h4>
				  	<p>
				  	Sur les versions récentes de Linux, il est possible de taper directement les caractères accentués
				  	en tapant ^ puis la lettre concernée.
				  	</p>
				  	<p>Si cela n'est pas disponible sur votre ordinateur, voici une méthode pour y remédier :<br/>
				  	Il suffit de créer un fichier .Xmodmap dans votre répertoire personnel, et d'y rajouter 
					les lignes qui suivent. Après ça, plus qu'à taper un petit xmodmap /home/votre_pseudo/.Xmodmap 
					dans un terminal… Maintenant appuyez sur la touche avec la fenêtre windows de votre clavier 
					puis sur la lettre espérantiste que vous voulez... </p>
					<p>keycode 115 = F13<br>
					keycode 30 = ubreve Ubreve<br>
					keycode 39 = scircumflex Scircumflex<br>
					keycode 42 = gcircumflex Gcircumflex<br>
					keycode 43 = hcircumflex Hcircumflex<br>
					keycode 44 = jcircumflex Jcircumflex<br>
					keycode 54 = ccircumflex Ccircumflex<br>
					keycode 115 = Mode_switch 				
				  	</p>
					
